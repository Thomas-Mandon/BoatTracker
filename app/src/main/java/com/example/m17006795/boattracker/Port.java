package com.example.m17006795.boattracker;

public class Port {
    private int id;
    private String name;
    private float latitude;
    private float longitude;

    public Port(int id, String name, float latitude, float longitude) {
        this.id = id;
        this.name = name;
        this.latitude = latitude;
        this.longitude = longitude;
    }
}
